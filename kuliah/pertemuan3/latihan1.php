<?php 
// Pengulangan

// for ---> lakukan dulu baru ngechek
for( $i = 0; $i < 5; $i++ ) {
	echo "For <br>";
}


// while ---> Selama kondisinya TRUE lakukan apapun yang ada didalamnya
$i = 0;
while( $i < 5 ) {
	echo "While <br>";
$i++; // increment
}


// do.. while ---> jika FALSE blok dijalankan satu kali
$i = 0 
do {
	echo "do.. while <br>";
$i++;
} while ( $i < 5 );






?>